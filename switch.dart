void main() {
  const weather = "crying";

  switch (weather) {
    case "cloudy":
      print("The weather is cloudy");
      break;
    case "sunny":
      print("The weather is sunny");
      break;
    case "rainy":
      print("The weather is rainy");
      break;

    default:
      print("The weather is neither cloudy, rainy nor sunny");
  }
}
